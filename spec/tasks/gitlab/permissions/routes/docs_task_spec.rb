# frozen_string_literal: true

require 'spec_helper'

RSpec.describe Tasks::Gitlab::Permissions::Routes::DocsTask, :silence_stdout, feature_category: :permissions do
  let(:task) { described_class.new }
  let(:path) { Rails.root.join('tmp/tests/doc/gitlab/permissions/routes/') }
  let(:doc) { 'granular_pat_rest_api_endpoints.md' }
  let(:doc_path) { Rails.root.join(path, doc) }
  let(:routes) do
    [
      delete_package_route,
      download_package_route,
      upload_package_route,
      read_job_route,
      multi_permission_route,
      skipped_route
    ]
  end

  let(:read_job_assignable) do
    instance_double(::Authz::PermissionGroups::Assignable,
      category_name: 'Another Category',
      resource_name: 'Job',
      resource_description: 'Grants the ability to read jobs',
      action: 'read',
      permissions: %i[read_job],
      deprecated?: false
    )
  end

  let(:read_package_assignable) do
    instance_double(::Authz::PermissionGroups::Assignable,
      category_name: 'Category',
      resource_name: 'Package',
      resource_description: 'Grants the ability to read, uploaad, and delete packages',
      action: 'read',
      permissions: %i[download_package delete_package],
      deprecated?: false
    )
  end

  let(:create_package_assignable) do
    instance_double(::Authz::PermissionGroups::Assignable,
      category_name: 'Category',
      resource_name: 'Package',
      resource_description: 'Grants the ability to read, uploaad, and delete packages',
      action: 'create',
      permissions: %i[upload_package],
      deprecated?: false
    )
  end

  let(:delete_package_assignable) do
    instance_double(::Authz::PermissionGroups::Assignable,
      category_name: 'Category',
      resource_name: 'Package',
      resource_description: 'Grants the ability to read, uploaad, and delete packages',
      action: 'delete',
      permissions: %i[delete_package],
      deprecated?: false
    )
  end

  before do
    allow(::API::API).to receive(:endpoints).and_return([instance_double(Grape::Endpoint, routes: routes)])
    allow(::Authz::PermissionGroups::Assignable).to receive(:all).and_return({
      create_package: create_package_assignable,
      read_package: read_package_assignable,
      read_job: read_job_assignable,
      delete_package: delete_package_assignable
    })

    allow(task).to receive(:doc_path).and_return(doc_path)
  end

  describe '#check_docs' do
    subject(:check_docs) { task.check_docs }

    before do
      FileUtils.mkdir_p(path)
      task.compile_docs
    end

    context 'when the docs are up to date' do
      it 'outputs a success message' do
        expect { check_docs }
          .to output("Granular Personal Access Token allowed endpoints documentation is up to date.\n")
          .to_stdout
      end
    end

    context 'when the doc is updated manually' do
      before do
        File.write(doc_path, 'Manually adding this line at the end of the the doc', mode: 'a+')
      end

      let(:error_message) do
        <<~OUTPUT
          ##########
          #
          # Granular Personal Access Token allowed endpoints documentation is outdated! Please update it by running `bundle exec rake gitlab:permissions:routes:compile_docs`.
          #
          ##########
        OUTPUT
      end

      it 'raises an error' do
        expect { check_docs }.to raise_error(SystemExit).and output(error_message).to_stdout
      end
    end

    context 'when the doc is not up to date' do
      before do
        allow(::API::API).to receive(:endpoints).and_return([instance_double(Grape::Endpoint, routes: [])])
      end

      let(:error_message) do
        <<~OUTPUT
          ##########
          #
          # Granular Personal Access Token allowed endpoints documentation is outdated! Please update it by running `bundle exec rake gitlab:permissions:routes:compile_docs`.
          #
          ##########
        OUTPUT
      end

      it 'raises an error' do
        expect { described_class.new.check_docs }.to raise_error(SystemExit).and output(error_message).to_stdout
      end
    end
  end

  describe '#compile_docs' do
    subject(:compile_docs) { task.compile_docs }

    before do
      FileUtils.mkdir_p(path)
    end

    it 'outputs a success message' do
      expect { compile_docs }
        .to output("Granular Personal Access Token allowed endpoints documentation compiled.\n")
        .to_stdout
    end

    it 'creates granular_pat_rest_api_endpoints.md', :aggregate_failures do
      FileUtils.rm_f(doc_path)
      expect { File.read(doc_path) }.to raise_error(Errno::ENOENT)

      compile_docs

      expect(File.read(doc_path)).to match(/This documentation is auto-generated by a Rake task/)
    end
  end

  describe '#allowed_endpoints' do
    let(:expected_markdown) do
      <<~MARKDOWN.chomp
        ### Another Category resources

        #### Job

        Grants the ability to read jobs

        | Action | Access | Method | Path |
        | ------ | ------ | ------ | ---- |
        | Read | Project | `GET` | `/path/to/read_job_route` |

        ### Category resources

        #### Package

        Grants the ability to read, uploaad, and delete packages

        | Action | Access | Method | Path |
        | ------ | ------ | ------ | ---- |
        | Create | Group | `PUT` | `/path/to/upload_package_route` |
        | Create | Instance | `PUT` | `/path/to/upload_package_route` |
        | Read | Project | `GET` | `/path/to/download_package_route` |
        | Read <sup>1</sup> | Project | `GET` | `/path/to/multi_permission_route` |
        | Read | Project | `DELETE` | `/path/to/delete_package_route` |

        <sup>1</sup> Also requires the `Read Job` permission.
      MARKDOWN
    end

    # 1. Categories are sorted alphabetically
    # 2. Resources are sorted alphabetically
    # 3. Routes are sorted by
    #    1. Action, alphabetically
    #    1. Boundary (see `BOUNDARY_SORT_ORDER`)
    #    2. Request method (see `REQUEST_METHOD_SORT_ORDER`)
    # 4. Routes with additional permissions are marked with a <sup> footnote
    it 'returns the expected markdown' do
      expect(task.allowed_endpoints).to eq(expected_markdown)
    end

    context 'when a raw permission exists in both a deprecated and active assignable permission' do
      let(:deprecated_read_job_assignable) do
        instance_double(::Authz::PermissionGroups::Assignable,
          category_name: 'Another Category',
          resource_name: 'Deprecated Job',
          resource_description: '',
          action: 'read',
          permissions: %i[read_job],
          deprecated?: true
        )
      end

      before do
        allow(::Authz::PermissionGroups::Assignable).to receive(:all).and_return({
          create_package: create_package_assignable,
          read_package: read_package_assignable,
          read_job: read_job_assignable,
          delete_package: delete_package_assignable,
          deprecated_read_job: deprecated_read_job_assignable
        })
      end

      it 'uses the non-deprecated assignable permission' do
        expect(task.allowed_endpoints).to include('#### Job')
        expect(task.allowed_endpoints).not_to include('Deprecated Job')
      end
    end

    context 'when the API route list contains duplicates' do
      let(:routes) { super() + [read_job_route] }

      it 'de-duplicates identical route rows' do
        markdown = task.allowed_endpoints

        expect(markdown.scan('`GET` | `/path/to/read_job_route`').length).to eq(1)
        expect(markdown).to eq(expected_markdown)
      end
    end

    context 'when multiple routes share resource, action, boundary, and request method but differ in path' do
      let(:download_package_route_a) do
        create_route_double(%i[download_package], :project, 'GET', '/api/:version/path/to/package_a')
      end

      let(:download_package_route_b) do
        create_route_double(%i[download_package], :project, 'GET', '/api/:version/path/to/package_b')
      end

      it 'orders rows the same way regardless of the input route order' do
        allow(::API::API).to receive(:endpoints).and_return(
          [instance_double(Grape::Endpoint, routes: [download_package_route_b, download_package_route_a])]
        )
        descending_input = described_class.new.allowed_endpoints

        allow(::API::API).to receive(:endpoints).and_return(
          [instance_double(Grape::Endpoint, routes: [download_package_route_a, download_package_route_b])]
        )
        ascending_input = described_class.new.allowed_endpoints

        expect(descending_input).to eq(ascending_input)
      end
    end
  end

  describe '#publicly_accessible_endpoints' do
    let(:public_anonymous_role) do
      instance_double(::Authz::Role).tap do |role|
        allow(role).to receive(:permissions).with(:project).and_return(Set.new(%i[download_package read_job]))
        allow(role).to receive(:permissions).with(:group).and_return(Set.new(%i[upload_package]))
      end
    end

    before do
      allow(::Authz::Role).to receive(:get).with(:public_anonymous).and_return(public_anonymous_role)
    end

    it 'lists routes whose permissions are all granted by public_anonymous for the route boundary' do
      expected = <<~MARKDOWN.chomp
        | Action | Method | Path |
        | ------ | ------ | ---- |
        | Package: Read | `GET` | `/path/to/download_package_route` |
        | Package: Read, Job: Read | `GET` | `/path/to/multi_permission_route` |
        | Job: Read | `GET` | `/path/to/read_job_route` |
        | Package: Create | `PUT` | `/path/to/upload_package_route` |
      MARKDOWN

      expect(task.publicly_accessible_endpoints).to eq(expected)
    end

    context 'when public_anonymous does not grant a route permission' do
      let(:public_anonymous_role) do
        instance_double(::Authz::Role).tap do |role|
          allow(role).to receive(:permissions).with(:project).and_return(Set.new(%i[read_job]))
          allow(role).to receive(:permissions).with(:group).and_return(Set.new)
        end
      end

      it 'excludes routes that require an ungranted permission' do
        markdown = task.publicly_accessible_endpoints

        expect(markdown).to include('/path/to/read_job_route')
        expect(markdown).not_to include('/path/to/download_package_route')
        expect(markdown).not_to include('/path/to/multi_permission_route')
        expect(markdown).not_to include('/path/to/upload_package_route')
      end
    end
  end

  describe '#skipped_endpoints' do
    let(:expected_markdown) do
      <<~MARKDOWN.chomp
        | Method | Path | Reason |
        | ------ | ---- | ------ |
        | `POST` | `/path/to/skipped_route` | CI job token |
      MARKDOWN
    end

    it 'returns the expected markdown' do
      expect(task.skipped_endpoints).to eq(expected_markdown)
    end

    context 'when the API route list contains duplicates' do
      let(:routes) { super() + [skipped_route] }

      it 'de-duplicates identical route rows' do
        markdown = task.skipped_endpoints

        expect(markdown.scan('`POST` | `/path/to/skipped_route`').length).to eq(1)
        expect(markdown).to eq(expected_markdown)
      end
    end

    context 'when skip_granular_token_authorization is true instead of a symbol' do
      let(:routes) { [skipped_route_without_reason] }

      it 'renders without a reason label' do
        expected = <<~MARKDOWN.chomp
          | Method | Path | Reason |
          | ------ | ---- | ------ |
          | `GET` | `/path/to/legacy_skipped_route` |  |
        MARKDOWN

        expect(task.skipped_endpoints).to eq(expected)
      end
    end
  end

  def create_route_double(permissions, boundary_type, request_method, path)
    instance_double(Grape::Router::Route,
      settings: {
        authorization: {
          permissions: permissions,
          boundary_type: boundary_type
        }
      },
      request_method: request_method,
      origin: path
    )
  end

  def read_job_route
    create_route_double(%i[read_job], :project, 'GET', '/api/:version/path/to/read_job_route')
  end

  def delete_package_route
    create_route_double(%i[delete_package], :project, 'DELETE', '/api/:version/path/to/delete_package_route')
  end

  def download_package_route
    create_route_double(%i[download_package], :project, 'GET', '/api/:version/path/to/download_package_route')
  end

  def upload_package_route
    instance_double(Grape::Router::Route,
      settings: {
        authorization: {
          permissions: %i[upload_package],
          boundaries: [{ boundary_type: :group }, { boundary_type: :instance }]
        }
      },
      request_method: 'PUT',
      origin: '/api/:version/path/to/upload_package_route'
    )
  end

  def multi_permission_route
    create_route_double(%i[download_package read_job], :project, 'GET', '/api/:version/path/to/multi_permission_route')
  end

  def skipped_route
    instance_double(Grape::Router::Route,
      settings: {
        authorization: {
          skip_granular_token_authorization: :job_token_auth
        }
      },
      request_method: 'POST',
      origin: '/api/:version/path/to/skipped_route'
    )
  end

  def skipped_route_without_reason
    instance_double(Grape::Router::Route,
      settings: {
        authorization: {
          skip_granular_token_authorization: true
        }
      },
      request_method: 'GET',
      origin: '/api/:version/path/to/legacy_skipped_route'
    )
  end
end
